#include <stdio.h>

int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (tab)
	{
		while (*(tab + i))
		{
			if ((*f)(*(tab + i)) == 1)
				j++;
			i++;
		}
	}
	return (j);
}
